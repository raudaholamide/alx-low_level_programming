#include "main.h"

/**
 * add - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtracts one integer from another.
 * @b: The first integer to be sub from.
 * @b: The second integer to sub from first.
 *
 * Return: The result of the subtraction.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies two integers.
 * @a: The first integer to be.
 * @b: The second integer to be.
 *
 * Return: The result of the multiplication.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides one integer by another.
 * @a: The numerator.
 * @b: The denominator.
 *
 * Return: The result of the multiplication.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Finds remaider of division of two integers.
 * @a: The numerator.
 * @b: The denominator.
 *
 * Return: The remainder from the division.
 */
int mod(int a, int b)
{
	return (a % b);
}
